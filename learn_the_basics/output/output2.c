#include <stdio.h>

int main(void){
	
	int a; //creates a variable. For numbers, we will usually use the type integer(int)
	a = 20; //associates the number 20 with the variable a
	printf("%d", a); // %d means decimal integer
	
	printf("\n"); //  \n means new line
	/*
		output:
		20
		
	*/
	
	int b;
	b = 5;
	printf("%d", a+b);
	printf("\n");
	
	/*
		output:
		25
		
	*/
	
	
	
}
